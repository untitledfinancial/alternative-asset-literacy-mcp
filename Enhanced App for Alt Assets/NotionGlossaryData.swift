                usedInModules: ["esg_climate"],
                usedInSections: [],
                category: .esg,
                userNotes: nil
            )
        }
        allTerms.append(contentsOf: esgTerms)

        // 3. DeFi glossary terms from DeFiLearningManager
        let defiModuleTerms = DeFiLearningManager.shared.glossary.enumerated().map { index, concept in
            GlossaryTerm(
                id: "defi_module_term_\(index)",
                term: concept.term,
                definition: concept.definition,
                simpleExplanation: nil,
                examples: [],
                relatedTerms: [],
                usedInModules: ["defi"],
                usedInSections: [],
                category: .defi,
                userNotes: nil
            )
        }
        allTerms.append(contentsOf: defiModuleTerms)

        // 4. Deduplicate by term name (case-insensitive), keeping the first occurrence
        var seen = Set<String>()
        var unique: [GlossaryTerm] = []
        for term in allTerms {
            let key = term.term.lowercased().trimmingCharacters(in: .whitespaces)
            if !seen.contains(key) {
                seen.insert(key)
                unique.append(term)
            }
        }

        // 5. Sort alphabetically by term name
        return unique.sorted { $0.term.localizedCaseInsensitiveCompare($1.term) == .orderedAscending }
    }
